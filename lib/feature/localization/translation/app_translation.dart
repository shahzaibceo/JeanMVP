extension TranslationExtension on String {
  /// Context-less translation: uses `LocalizationService.instance.selectedLanguageCode`.
  String tr() {
    final langCode = LocalizationService.instance.selectedLanguageCode;
    return AppTranslations.translations[langCode]?[this] ?? this;
  }

  /// Translation with a BuildContext (keeps backward compatibility where needed).
  String trWithContext(BuildContext context) {
    final langCode = context.read<LanguageCubit>().state.selectedLanguageCode;
    return AppTranslations.translations[langCode]?[this] ?? this;
  }
}
